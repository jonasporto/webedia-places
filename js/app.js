var places = [
{
  title: "Webedia França",
  phone: "+33 1 84 20 09 84",
  site: "https://webedia.fr",
  address: "2 Rue Paul Vaillant Couturier, 92300 Levallois-Perret, França",
  coords: {
  	lat: 48.8933176,
  	lng: 2.2755299
  } 
}];

(function(styles){


	function initialize() {
		
		var coords = places[0].coords;
		var myLatlng = new google.maps.LatLng(coords.lat, coords.lng);
		var imagePath = 'http://www.webedia-group.com/web/skins/default/images/favicon.ico'
		var mapOptions = {
			zoom: 2,
			center: myLatlng,
			mapTypeId: google.maps.MapTypeId.ROADMAP,
			styles: styles
		}

		var map = new google.maps.Map(document.getElementById('map'), mapOptions);
		
		var contentString = 'Some address here..';
		//Set window width + content
		var infowindow = new google.maps.InfoWindow({
			content: contentString,
			maxWidth: 500
		});

		//Add Marker
		var marker = new google.maps.Marker({
			position: myLatlng,
			map: map,
			icon: imagePath,
			title: 'image title',
			backgroundColor: "#ccc"
		});

		google.maps.event.addListener(marker, 'click', function() {
			infowindow.open(map,marker);
		});

		//Resize Function
		google.maps.event.addDomListener(window, "resize", function() {
			var center = map.getCenter();
			google.maps.event.trigger(map, "resize");
			map.setCenter(center);
		});

	}
	
	google.maps.event.addDomListener(window, 'load', initialize);

})(styles);
